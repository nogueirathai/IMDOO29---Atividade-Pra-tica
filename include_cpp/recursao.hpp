#ifndef RECURSAO_HPP
#define RECURSAO_HPP

#include <string>

/**
 * @brief Conta recursivamente as ocorrências de um caractere em uma string.
 * * @param str A string na qual a busca será feita.
 * @param alvo O caractere a ser contado.
 * @return O número de vezes que o 'alvo' aparece na 'str'.
 */
int recursao(const std::string &str, char alvo);

#endif // RECURSAO_HPP